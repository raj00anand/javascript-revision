//Variable
//let, const, var
//we can declare variable using three ways in  javaScript

//we can initialse value using var keyword.
var a=10;
var a=9;
console.log(a**2);

//we can change the value of variable but we cannot reinitialise using let keyword.
let b=9;

console.log(b);
b=5;
console.log(b);

//it is used to declare constant so we cannot change the value as well as reinitailise.
const c=3;
console.log(c);